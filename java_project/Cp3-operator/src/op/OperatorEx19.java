package op;

public class OperatorEx19 {

	public static void main(String[] args) {
		int share = 10/8; // int 타입 변수 share 에 10나누기 8 값 저장 정수타입인 1 만남음
		int remain = 10 % 8; // int 타입 변수 remain에 10 나누기8의 나머지값 저장 정수타입인 2만남음 
		System.out.println("10을 8로 나누면," ); 
		System.out.println("몫은 "+ share + "이고, 나머지는"+remain+ " 입니다."); // 1이랑 2추력 

	}

}
