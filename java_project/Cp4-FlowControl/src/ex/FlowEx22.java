package ex;

public class FlowEx22 {

	public static void main(String[] args) {
		int i=2;
		// i에 2저장 
		while(i<=9) {
			// i가 9이하이면 아래 돌림
		int j=1;
		// j에 1저장
		while(j <=9) {
			// j가 9이하면 아래 돌림
		System.out.println( i +" * "+ j + " = " + i*j );
		j++; // j를 1씩돌림
		}
		i++; // j 1~9까지 다 더하고 i 1돌림
		} // 스위치끝냄

	}

}
