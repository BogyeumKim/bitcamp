package ex;

public class FlowEx16 {

	public static void main(String[] args) {
		for(int i=2; i <=9; i++) {
			// i는 2부터 9까지 1씩더하면서 반복함
			for(int j=1; j <=9; j++) {
				//i가 1번실행되면 바로 j가 1부터 9까지 1씩더해지면서 얘먼저함
			System.out.println( i +" * "+ j + " = " + i*j );
			}
			}
		// j가 9까지돌면 i는 1이 더해진다고생각

	}

}
