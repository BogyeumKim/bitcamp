package ex;

public class BooleanVar {

	public static void main(String[] args) {
		boolean b1=true; // 블룬타입 b1변수에 트루 저장
		boolean b2=false; // 블룬타입b2변수에 펄스 저장
		System.out.println(b1); // 트루
		System.out.println(b2); // 펄스
		System.out.println(3<4); // 4가 더크니까 트루
		System.out.println(3>4); // 3이 더 작으니까 펄스 
	}

}
