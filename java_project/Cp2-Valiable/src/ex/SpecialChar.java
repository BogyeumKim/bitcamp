package ex;

public class SpecialChar {

	public static void main(String[] args) {
		char single = '\'';  // char타입 변수single에 ' 저장
		String dblQuote = "\"Hello\""; // String타입[문구] 변수dblQuote에 "Hello" 문구 저장
		String root = "c:\\"; // String타입 변수 root에는 c:\ 문구 저장
		System.out.println(single); // sing '출력
		System.out.println(dblQuote); // dblQuote "Hello" 출력
		System.out.println(root); // root c:\ 출력


	}

}
